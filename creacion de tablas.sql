CREATE TABLE SUPPLIERS (
    SNUM NUMBER PRIMARY KEY,
    SNAME VARCHAR(20) NOT NULL,
    STATUS NUMBER NOT NULL,
    CITY VARCHAR(20) NOT NULL
);

CREATE TABLE PARTS (
    PNUM NUMBER PRIMARY KEY,
    PNAME VARCHAR(20) NOT NULL,
    COLOR VARCHAR(10) NOT NULL,
    WEIGHT NUMBER NOT NULL,
    CITY VARCHAR(20) NOT NULL
);

CREATE TABLE SHIPMENTS (
    SNUM NUMBER,
    PNUM NUMBER,
    QTY NUMBER NOT NULL,
    PRIMARY KEY (SNUM, PNUM),
    FOREIGN KEY (SNUM) REFERENCES SUPPLIERS(SNUM),
    FOREIGN KEY (PNUM) REFERENCES PARTS(PNUM)
);

CREATE TABLE PROJECTS (
    JNUM NUMBER PRIMARY KEY,
    JNAME VARCHAR(20) NOT NULL,
    CITY VARCHAR(20) NOT NULL
);

CREATE TABLE SHIPMENTS_TO_PROJECTS (
    SNUM NUMBER,
    PNUM NUMBER,
    JNUM NUMBER,
    QTY NUMBER NOT NULL,
    PRIMARY KEY (SNUM, PNUM, JNUM),
    FOREIGN KEY (SNUM) REFERENCES SUPPLIERS(SNUM),
    FOREIGN KEY (PNUM) REFERENCES PARTS(PNUM),
    FOREIGN KEY (JNUM) REFERENCES PROJECTS(JNUM)
);

